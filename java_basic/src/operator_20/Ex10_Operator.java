package operator_20;

public class Ex10_Operator {
  
  public static void main(String[] args) {
    
    int num1 = 12;
    int num2 = 8;
    
    //+ - * / %
    int result1 = num1 + num2;//20
    int result2 = num1 - num2;//4
    int result3 = num1 * num2;//96
    int result4 = num1 / num2;// 몫 1
    int result5 = num1 % num2;// 나머지 4
    
    
    System.out.println("result1 = " + result1);
    System.out.println("resul2 = " + result2);
    System.out.println("resul3 = " + result3);
    System.out.println("resul4 = " + result4);
    System.out.println("resul5 = " + result5);
  }

}
